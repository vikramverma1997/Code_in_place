"""
File: average2numbers.py
------------------------
This program asks user to input two numbers and computes their average.
"""


def main():
    print("This program calculates average of two numbers: ")
    num1 = float(input("Enter first number: "))
    num2 = float(input("Enter second number: "))
    avg = (num1 + num2) / 2
    print("The average of two numbers is: " + str(avg) + ".")













if __name__ == '__main__':
    main()